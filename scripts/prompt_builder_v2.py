CONTINUITY_RULES = """
## 分段生成注意事项

- 直接从新内容开始，不要说"继续上一部分"、"接上文"
- 不要重复上一段已覆盖的内容
- 保持独立完整，无需过渡语句
- 这是脚本化系统，不是对话——流畅地输出笔记内容即可
"""

# ============================================================================
# V2 提示词构建器
# ============================================================================

class PromptBuilderV2:
    """
    Lore Engine 风格的提示词构建器 V2

    使用方法：
        prompt = (PromptBuilderV2()
            .with_mode("video_tutorial")
            .with_depth("balanced")
            .with_screenshots(screenshots)
            .with_inference()
            .with_summary()
            .build())
    """

    def __init__(self):
        self.parts = [BASE_INSTRUCTIONS]
        self._mode = None
        self._depth = None
        self._screenshots = []
        self._has_transcript = False

    def with_mode(self, mode: str) -> "PromptBuilderV2":
        """设置内容类型模式"""
        if mode in CONTENT_MODES:
            self._mode = mode
            self.parts.append(CONTENT_MODES[mode])
        return self

    def with_depth(self, depth: str) -> "PromptBuilderV2":
        """设置输出深度"""
        if depth in DEPTH_LEVELS:
            self._depth = depth
            self.parts.append(DEPTH_LEVELS[depth])
        return self

    def with_screenshots(self, screenshots: list) -> "PromptBuilderV2":
        """设置截图列表"""
        self._screenshots = screenshots

        # 生成截图列表
        screenshot_list = []
        for i, s in enumerate(screenshots, 1):
            safe_ts = s["timestamp"].replace(":", "-")
            screenshot_list.append(
                f"{i}. [{s['timestamp']}] {s.get('type', 'other')}: {s['desc']}\n"
                f"   路径: screenshots/{safe_ts}.jpg"
            )

        # 替换占位符
        integration = SCREENSHOT_INTEGRATION.replace(
            "{screenshot_list}",
            "\n".join(screenshot_list) if screenshot_list else "（无截图）"
        )
        self.parts.append(integration)
        return self

    def with_transcript(self, transcript_text: str) -> "PromptBuilderV2":
        """
        添加音频转录文本（三阶段管道核心）

        Args:
            transcript_text: 带时间戳的转录文本，格式如：
                [00:00-00:15] Welcome to CS61C...
                [00:15-00:30] Today we'll cover...
        """
        self._has_transcript = True

        integration = TRANSCRIPT_INTEGRATION.replace(
            "{transcript_text}",
            transcript_text if transcript_text else "（无转录文本）"
        )
        self.parts.append(integration)
        return self

    def with_differential_captions(self, captions_text: str) -> "PromptBuilderV2":
        """
        添加差异化描述（注意力锚点）— Stage 2b 输出

        Args:
            captions_text: 格式化的差异描述文本，来自
                differential_captioner.format_captions_for_prompt()
        """
        if captions_text:
            section = DIFFERENTIAL_CAPTIONS_SECTION.replace(
                "{differential_captions}",
                captions_text
            )
            self.parts.append(section)
        return self

    def with_inference(self) -> "PromptBuilderV2":
        """启用智能推理补全"""
        self.parts.append(INTELLIGENT_INFERENCE)
        return self

    def with_hierarchy(self) -> "PromptBuilderV2":
        """启用分层结构组织（lecture 模式使用讲课顺序）"""
        if self._mode == "lecture":
            self.parts.append(LECTURE_STRUCTURE)
        else:
            self.parts.append(HIERARCHICAL_STRUCTURE)
        return self

    def with_summary(self) -> "PromptBuilderV2":
        """添加 AI 摘要"""
        self.parts.append(AI_SUMMARY)
        return self

    def with_latex(self) -> "PromptBuilderV2":
        """启用 LaTeX 数学公式支持"""
        self.parts.append(LATEX_SUPPORT)
        return self

    def with_tables(self) -> "PromptBuilderV2":
        """启用表格格式化指南"""
        self.parts.append(TABLE_FORMATTING)
        return self

    def with_tricky_questions(self) -> "PromptBuilderV2":
        """启用理解检测题"""
        self.parts.append(TRICKY_QUESTIONS)
        return self

    def with_teaching_scaffolding(self) -> "PromptBuilderV2":
        """启用教学脚手架（讲座模式推荐，确保 blockquote/examples/对比/总结）"""
        self.parts.append(TEACHING_SCAFFOLDING)
        return self

    def with_continuity(self) -> "PromptBuilderV2":
        """分段生成时禁止重复和过渡语句"""
        self.parts.append(CONTINUITY_RULES)
        return self

    def build(self) -> str:
        """构建最终提示词"""
        return "\n\n".join(self.parts)

    def __str__(self) -> str:
        return (f"PromptBuilderV2(mode={self._mode}, depth={self._depth}, "
                f"screenshots={len(self._screenshots)}, transcript={self._has_transcript})")


